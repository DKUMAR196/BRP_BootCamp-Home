package com.bridgelabz.program_day03;

public class Stats5 {
	static void stats05() {
		System.out.println("Random number between 0 and 1 : " + Math.random());
		System.out.println("Max value between 0 and 1 : " + Math.max(0, 1));
		System.out.println("The min value between 0 and 1: " + Math.min(0, 1));
	}
	
	public static void main(String[] args) {
		stats05();
	}
}
